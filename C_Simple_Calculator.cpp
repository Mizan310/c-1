#include <bits/stdc++.h>
using namespace std;

int main()
{
    long long int x, y, ans;
    cin >> x >> y;
    ans = x + y;
    cout << x << " + " << y << " = " << ans << endl;
    ans = x * y;
    cout << x << " * " << y << " = " << ans << endl;
    ans = x - y;
    cout << x << " - " << y << " = " << ans << endl;
    return 0;
}